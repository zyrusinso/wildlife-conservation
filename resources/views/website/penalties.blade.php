                        <b>SEC. 3. A new Section 7 is hereby inserted after Section 6 of the same Act to read as follows:</b><br/><br/>

                        “SEC. 7. It shall be unlawful for any person who has custody of an animal to abandon the <br/>animal.<br/><br/>

                        “If any person being the owner or having charge or control of any animal shall without <br/>reasonable cause or excuse abandon it, whether permanently or not, without providing <br/>for the care of that animal, such act shall constitute maltreatment under Section 9.<br/><br/>

                        “If the animal is left in circumstances likely to cause the animal any unnecessary suffering, <br/>or if this abandonment results in the death of the animal, the person liable shall suffer the <br/>maximum penalty.<br/><br/>

                        “Abandonment means the relinquishment of all right, title, claim, or possession of the <br/>animal with the intention of not reclaiming it or resuming its ownership or possession.”<br/><br/>

                        <b>SEC. 4. Section 8 of Republic Act No. 8485 which shall now become Section 9 is hereby amended to read as follows:</b><br/><br/>

                        “SEC. 9. Any person who subjects any animal to cruelty, maltreatment or neglect shall, <br/>upon conviction by final judgment, be punished by imprisonment and/ or fine, as indicated <br/>in the following graduated scale:<br/><br/>

                        “(1) Imprisonment of one (1) year and six (6) months and one (1) day to two (2) years and/or a fine not exceeding One hundred thousand pesos (P100,000.00) <b>if the animal subjected to cruelty</b>, maltreatment or neglect dies;<br/><br/>

                        “(2) Imprisonment of one (1) year and one (1) day to one (1) year and six (6) months <br/>and/or a fine not exceeding Fifty thousand pesos (P50,000.00) if the animal subjected to <br/>cruelty, maltreatment or neglect survives but is severely injured with loss of its natural <br/>faculty to survive on its own and needing human intervention to sustain its life; and<br/><br/>

                        “(3) Imprisonment of six (6) months to one (1) year and/or a fine not exceeding Thirty <br/>thousand pesos (P30,000.00) for subjecting any animal to cruelty, maltreatment or <br/>neglect but without causing its death or incapacitating it to survive on its own.<br/><br/>

                        “If the violation is committed by a juridical person, the officer responsible thereof shall <br/>serve the imprisonment. If the violation is committed by an alien, he or she shall be <br/>immediately deported after the service of sentence without any further proceeding.<br/><br/>

                        “The foregoing penalties shall also apply for any other violation of this Act, depending <br/>upon the effect or result of the act or comission as defined in the immediately preceding <br/>sections.<br/><br/>
                        
                        “However, regardless of the resulting condition to the animal/s, the penalty of two (2) <br/>years and one (1) day to three (3) years and/or a fine not exceeding Two hundred fifty <br/>thousand pesos (P250,000.00) shall be imposed if the offense is committed by any of the <br/>following: (1) a syndicate; (2) an offender who makes business out of cruelty to an animal; <br/>(3) a public officer or employee; or (4) where at least three (3) animals are involved.<br/><br/>

                        “In any of the foregoing situations, the offender shall suffer subsidiary imprisonment in <br/>case of insolvency and the inability to pay the fine.”<br/><br/>

                        <b>SEC. 5. A new Section 10 is hereby inserted after the Section above to read as follows:</b><br/><br/>

                        “SEC. 10. The Secretary of the Department of Agriculture shall deputize animal welfare <br/>enforcement officers from nongovernment organizations, citizens groups, community <br/>organizations and other volunteers who have undergone the necessary training for this <br/>purpose. The Philippine National Police, the National Bureau of Investigation and other <br/>law enforcement agencies shall designate animal welfare enforcement officers. As such, <br/>animal welfare enforcement officers shall have the authority to seize and rescue illegally <br/>traded and maltreated animals and to arrest violators of this Act subject to the guidelines <br/>of existing laws and rules and regulations on arrest and detention.<br/><br/>

                        “The Secretary of the Department of Agriculture shall, upon the recommendation of the <br/>Committee on Animal Welfare:<br/><br/>

                        “(1) Promulgate the guidelines on the criteria and training requirements for the <br/>deputization of animal welfare enforcement officers; and<br/><br/>

                        “(2) Establish a mechanism for the supervision, monitoring and reporting of these <br/>enforcement officers.”<br/><br/>

                        <b>SEC. 6.</b> If, for any reason, any provision of this Act is declared to be unconstitutional or <br/>invalid, the other sections or provisions hereof which are not affected shall continue to be <br/>in full force and effect.<br/><br/>

