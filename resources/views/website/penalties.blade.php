                        <b>SECTION 1. Section 1 of Republic Act No. 8485 is hereby amended to read as follows;</b><br/><br/>
                        
                        “SECTION 1. It is the purpose of this Act to protect and promote the welfare of all <br/>terrestrial, aquatic and marine animals in the Philippines by supervising and regulating <br/>the establishment and operations of all facilities utilized for breeding, maintaining, <br/>keeping, treating or training of all animals either as objects of trade or as household pets. <br/>For purposes of this Act, pet animal shall include birds.<br/><br/>

                        “For purposes of this Act, animal welfare pertains to the physical and psychological well-<br/>being of animals. It includes, but not limited to, the avoidance of abuse, maltreatment, <br/>cruelty and exploitation of animals by humans by maintaining appropriate standards of <br/>accommodation, feeding and general care, the prevention and treatment of disease and <br/>the assurance of freedom from fear, distress, harassment, and unnecessary discomfort <br/>and pain, and allowing animals to express normal behavior.”<br/><br/>

                        <b>SEC. 2. Section 6 of Republic Act No. 8485 is hereby amended to read as follows:</b><br/><br/>

                        “SEC. 6. It shall be unlawful for any person to torture any animal, to neglect to provide <br/>adequate care, sustenance of shelter, or maltreat any animal or to subject any dog or <br/>horse to dogfights or horsefights, kill or cause or procure to be tortured or deprived of <br/>adequate care, sustenance or shelter, or maltreat or use the same in research or <br/>experiments not expressly authorized by the Committee on Animal Welfare.<br/><br/>

                        “The killing of any animal other than cattle, pigs, goats, sheep, poultry, rabbits, carabaos <br/>and horses is likewise hereby declared unlawful except in the following instances:<br/><br/>

