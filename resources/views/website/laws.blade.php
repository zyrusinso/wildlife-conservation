                <div class="card-header">
                    <h1 class="_title" >LAWS</h1>
                    <h4>REPUBLIC ACT NO. 8485</h4>
                </div>

                <div class="card-body" style="text-align: center; color: white;">
                    <h5>
                        AN ACT TO PROMOTE ANIMAL WELFARE IN THE PHILIPPINES, OTHERWISE KNOWN AS “THE ANIMAL WELFARE ACT OF 1998”
                    </h5>

                    <p>
                        Be it enacted by the Senate and House of Representatives of the Philippines in Congress assembled:<br/><br/>

                        <b>SECTION 1.</b> It is the purpose of this Act to protect and promote the welfare of all animals in the Philippines by supervising and regulating the establishment and operations of all facilities utilized for breeding, maintaining, keeping, treating or training of all animals either as objects of trade or as household pets. For purposes of this Act, pet animal shall include birds.<br/><br/>
                        
                        <b>SECTION 2.</b> No person, association, partnership, corporation, cooperative or any government agency or instrumentality including slaughter houses shall establish, maintain and operate any pet shop, kennel, veterinary clinic, veterinary hospital, stockyard, corral, stud farm or stock farm or zoo for the breeding, treatment, sale or trading, or training of animals without first securing from the Bureau of Animal Industry a certificate of registration therefor.<br/><br/>
                        
                        The certificate shall be issued upon proof that the facilities of such establishment for animals are adequate, clean and sanitary and will not be used for, nor cause pain and/or suffering to the animals. The certificate shall be valid for a period of one (1) year unless earlier cancelled for just cause before the expiration of its term by the Director of the Bureau of Animal Industry and may be renewed from year to year upon compliance with the conditions imposed hereunder. The Bureau shall charge reasonable fees for the issuance or renewal of such certificate.<br/><br/>
                        
                        The condition that such facilities be adequate, clean and sanitary, and that they will not be used for nor cause pain and/or suffering to the animals is a continuing requirement for the operation of these establishments. The Bureau may revoke or cancel such certificate of registration for failure to observe these conditions and other just causes.<br/><br/>
                        
                        <b>SECTION 3.</b> The Director of the Bureau of Animal Industry shall supervise and regulate the establishment, operation and maintenance of pet shops, kennels, veterinary clinics, veterinary hospitals, stockyards, corrals, stud farms and zoos and any other form or structure for the confinement of animals where they are bred, treated, maintained, or kept either for sale or trade or for training purposes as well as the transport of such animals in any form of public or private transportation facility in order to provide maximum comfort while in transit and minimize, if not totally eradicate, incidence of sickness and death and prevent any cruelty from being inflicted upon the animals.<br/><br/>
                        
                        The Director may call upon any government agency for assistance consistent with its powers, duties, and responsibilities for the purpose of ensuring the effective and efficient implementation of this Act and the rules and regulations promulgated thereunder.<br/><br/>
                        
                        It shall be the duty of such government agency to assist said Director when called upon for assistance using any available fund in its budget for the purpose.<br/><br/>
                        
                        <b>SECTION 4.</b> It shall be the duty of any owner or operator of any land, air or water public utility transporting pet, wildlife and all other animals to provide in all cases adequate, clean and sanitary facilities for the safe conveyance and delivery thereof to their consignee at the place of consignment. They shall provide sufficient food and water for such animals while in transit for more than twelve (12) hours or whenever necessary.<br/><br/>
                        
                        No public utility shall transport any such animal without a written permit from the Director of the Bureau of Animal Industry or his/her authorized representative. No cruel confinement or restraint shall be made on such animals while being transported.<br/><br/>
                        
                        Any form of cruelty shall be penalized even if the transporter has obtained a permit from the Bureau of Animal Industry. Cruelty in transporting includes overcrowding, placing of animals in the trunks or under the hood trunks of the vehicles.<br/><br/>
                        
                        <b>SECTION 5.</b> There is hereby created a Committee on Animal Welfare attached to the Department of Agriculture which shall, subject to the approval of the Secretary of the Department of Agriculture, issue the necessary rules and regulations for the strict implementation of the provisions of this Act, including the setting of safety and sanitary standards, within thirty (30) calendar days following its approval. Such guidelines shall be reviewed by the Committee every three (3) years from its implementation or whenever necessary.<br/><br/>
                        
                        The Committee shall be composed of the official representatives of the following:<br/><br/>

                        (1)   The Department of Interior and Local Government (DILG);<br/>
                        (2)   Department of Education, Culture and Sports (DECS);<br/>
                        (3)   Bureau of Animal Industry (BAI) of the Department of Agriculture (DA);<br/>
                        (4)   Protected Areas and Wildlife Bureau (PAWB) of the Department of Environment and Natural Resources (DENR);<br/>
                        (5)   National Meat Inspection Commission (NMIC) of the DA;<br/>
                        (6)   Agriculture Training Institute (ATI) of the DA;<br/>
                        (7)   Philippine Veterinary Medical Association (PVMA);<br/>
                        (8)   Veterinary Practitioners Association of the Philippines (VPAP);<br/>
                        (9)   Philippine Animal Hospital Association of the Philippines (PAHA);<br/>
                        (10)   Philippine Animal Welfare Society (PAWS);<br/>
                        (11)   Philippine Society for the Prevention of Cruelty to Animals (PSPCA);<br/>
                        (12)   Philippine Society of Swine Practitioners (PSSP);<br/>
                        (13)   Philippine College of Canine Practitioners (PCCP); and<br/>
                        (14)   Philippine Society of Animal Science (PSAS).<br/><br/>
                        
                        The Committee shall be chaired by a representative coming from the private sector and shall have two (2) vice chairpersons composed of the representative of the BAI and another from the private sector.<br/><br/>
                        
                        The Committee shall meet quarterly or as often as the need arises. The Committee members shall not receive any compensation but may receive reasonable honoraria from time to time.<br/><br/>
                        
                        <b>SECTION 6.</b> It shall be unlawful for any person to torture any animal, to neglect to provide adequate care, sustenance or shelter, or maltreat any animal or to subject any dog or horse to dogfights or horsefights, kill or cause or procure to be tortured or deprived of adequate care, sustenance or shelter, or maltreat or use the same in research or experiments not expressly authorized by the Committee on Animal Welfare.<br/><br/>
                        
                        The killing of any animal other than cattle, pigs, goats, sheep, poultry, rabbits, carabaos, horses, deer and crocodiles is likewise hereby declared unlawful except in the following instances:<br/><br/>
                        
                        (1)   When it is done as part of the religious rituals of an established religion or sect or a ritual required by tribal or ethnic custom of indigenous cultural communities; however, leaders shall keep records in cooperation with the Committee on Animal Welfare;<br/>
                        (2)   When the pet animal is afflicted with an incurable communicable disease as determined and certified by a duly licensed veterinarian;<br/>
                        (3)   When the killing is deemed necessary to put an end to the misery suffered by the animal as determined and certified by a duly licensed veterinarian;<br/>
                        (4)   When it is done to prevent an imminent danger to the life or limb of a human being; and<br/>
                        (5)   When done for the purpose of animal population control;<br/>
                        (6)   When the animal is killed after it has been used in authorized research or experiments; and<br/>
                        (7)   Any other ground analogous to the foregoing as determined and certified by a licensed veterinarian.<br/><br/>
                        
                        In all the above mentioned cases, including those of cattle, pigs, goats, sheep, poultry, rabbits, carabaos, horses, deer and crocodiles the killing of the animals shall be done through humane procedures at all times.<br/><br/>
                        
                        For this purpose, humane procedures shall mean the use of the most scientific methods available as may be determined and approved by the Committee.<br/><br/>
                        
                        Only those procedures approved by the Committee shall be used in the killing of animals.<br/><br/>
                        
                        <b>SECTION 7.</b> It shall be the duty of every person to protect the natural habitat of the wildlife. The destruction of said habitat shall be considered as a form of cruelty to animals and its preservation is a way of protecting the animals.<br/><br/>
                        <b>SECTION 8.</b> Any person who violates any of the provisions of this Act shall, upon conviction by final judgment, be punished by imprisonment of not less than six (6) months nor more than two (2) years or a fine or not less than One thousand pesos (P1,000) nor more than Five thousand pesos (P5,000) or both at the discretion of the Court. If the violation is committed by a juridical person, the officer responsible therefor shall serve the imprisonment when imposed. If the violation is committed by an alien, he or she shall be immediately deported after service of sentence without any further proceedings.<br/><br/>
                        <b>SECTION 9.</b> All laws, acts, decrees, executive orders, rules and regulations inconsistent with the provisions of this Act are hereby repealed or modified accordingly.<br/><br/>
                        <b>SECTION 10.</b> This Act shall take effect fifteen (15) days after its publication in at least two (2) newspapers of general circulation.<br/><br/>
                        
                        Approved on February 11, 1998.

